import { useContext, useEffect, useState } from "react";
import { ShopContext } from "../context/Shop-Context";
import Title from "./Title";
import ProductItem from "./ProductItem";

const LatestCollection = () => {
  const { products } = useContext(ShopContext);
  const [latestProducts, setLatestProducts] = useState<{ _id: string; name: string; description: string; price: number; image: string[]; category: string; subCategory: string; sizes: string[]; date: number; bestseller: boolean }[]>([]);

  useEffect(() => {
    setLatestProducts(products.slice(0, 10));
  }, [products]);

  return (
    <div className="my-10">
      <div className="text-center py-8 text-3xl">
        <Title text1={"LATEST"} text2={"COLLECTIONS"} />
        <p className="w-3/4 m-auto text-xs sm:text-sm md:text-base text-gray-600">Feast your eyes on the latest fashion trends happening right now. You can choose among the best and most versetile clothing accessories to fit your style!</p>
      </div>

      {/* Rendering Components */}

      <div className="grid grid-cols-2 sm:grid-cols-3 md:grid-cols-4 lg:grid-cols-5 gap-4 gap-y-6">
        {latestProducts.map((item, index) => (
          <ProductItem key={index} id={item._id} image={item.image} name={item.name} price={item.price} />
        ))}
      </div>
    </div>
  );
};

export default LatestCollection;
